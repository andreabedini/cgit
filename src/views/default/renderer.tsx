// Repo header band + tab strip. Reads the discovered repo and the active page
// straight off the request context, so handlers never thread navigation state
// through render props.
function RepoHeader() {
  const c = useRequestContext();
  const disc = c.get("disc");
  // No repo resolved (e.g. an error or 404 under /:repo/*): skip the band.
  if (!disc) return null;
  const name = disc.name;
  const path = c.req.path;
  const active =
    path.includes("/tree/") || path.includes("/raw/")
      ? "tree"
      : path.endsWith("/log/")
        ? "log"
        : "summary"; // summary, commit and diff all sit under the summary tab

  const base = c.env?.CGIT_CLONE_URL_BASE?.replace(/\/$/, "");
  const cloneUrl = base ? `${base}/${name}.git` : undefined;

  const tab = (page: "summary" | "log" | "tree", href: string) => (
    <a class={`cg-tab${active === page ? " active" : ""}`} href={href}>
      {page}
    </a>
  );

  return (
    <>
      <div class="cg-repohead">
        <div>
          <div class="cg-crumb">
            <a href="/">~</a>
            <span class="slash">/</span>
            <span class="name">{name}</span>
          </div>
          {disc.description ? <p class="desc">{disc.description}</p> : null}
        </div>
        {cloneUrl ? (
          <div class="cg-clone">
            <span class="label">git clone</span>
            <span class="url">{cloneUrl}</span>
            <button
              type="button"
              class="copy"
              title="Copy clone URL"
              {...{ onclick: `navigator.clipboard&&navigator.clipboard.writeText(${JSON.stringify(cloneUrl)})` }}
            >
              <svg viewBox="0 0 24 24" width="13" height="13" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">
                <rect x="9" y="9" width="11" height="11" rx="2"></rect>
                <path d="M5 15V5a2 2 0 0 1 2-2h10"></path>
              </svg>
            </button>
          </div>
        ) : null}
      </div>
      <nav class="cg-tabs">
        {tab("summary", `/${name}/`)}
        {tab("log", `/${name}/log/`)}
        {tab("tree", `/${name}/tree/`)}
      </nav>
    </>
  );
}

// Nested layout for repo pages: wraps the parent chrome and prepends the band.
export const repoLayout = jsxRenderer(({ children, Layout }) => (
  <Layout>
    <RepoHeader />
    {children}
  </Layout>
));
